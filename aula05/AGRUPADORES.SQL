-- AGRUPADORES.SQL
USE EMPRESA;

SELECT COUNT(*) AS TOTAL
	FROM FUNCIONARIOS
    WHERE SALARIO BETWEEN 1000 AND 2000;
    
SELECT FORMAT(SUM(SALARIO), 2, 'de_DE') AS TOTAL
	FROM FUNCIONARIOS;
    
SELECT FORMAT(AVG(SALARIO), 2, 'de_DE') AS MEDIA
	FROM FUNCIONARIOS;
    
SELECT FORMAT(MIN(SALARIO), 2, 'de_DE') AS MINIMO
	FROM FUNCIONARIOS;
    
SELECT FORMAT(MAX(SALARIO), 2, 'de_DE') AS MAXIMO
	FROM FUNCIONARIOS;
    

SELECT SEXO, COUNT(SEXO)
	FROM FUNCIONARIOS
    GROUP BY SEXO;
    
SELECT * FROM TELEFONES;    

SELECT OPERADORA, COUNT(OPERADORA)
	FROM TELEFONES
    WHERE DDD = 51
    GROUP BY OPERADORA;
    
    


    
    
    


    
    
    
	