-- VIEW.SQL
USE EMPRESA;

CREATE VIEW FOLHA_PAGAMENTO AS 
	SELECT NOME, SALARIO
		FROM FUNCIONARIOS
        ORDER BY NOME;

SELECT * FROM FOLHA_PAGAMENTO;

SHOW TABLES;

CREATE VIEW LISTA_TODOS AS
	SELECT F.NOME AS FUNCIONARIO, T.NUMERO, D.NOME AS DEPTO
	FROM FUNCIONARIOS AS F
    INNER JOIN TELEFONES AS T
		ON F.ID_FUNCIONARIO = T.ID_FUNCIONARIO
    INNER JOIN DEPARTAMENTOS AS D
		ON D.ID_DEPARTAMENTO = F.ID_DEPARTAMENTO
	ORDER BY F.NOME;

SELECT * FROM LISTA_TODOS;    

DROP VIEW LISTA_TODOS;
	







        
	
